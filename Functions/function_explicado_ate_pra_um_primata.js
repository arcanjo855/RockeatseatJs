function fazerSuco(fruta1, fruta2, fruta3) {
  // vc cria variaveis e coloca entre parenteses
  return `suco de:${fruta1}${fruta2}${fruta3}`; // o que vc quer fazer com as variaveis criadas
}

const copo = fazerSuco('banana', 'maça', 'manga'); // precisa criar uma variavel para o resultado da funçao

